def pomodoro_timer():
    print("\n--------- Menu ----------")
    print("1) Pomodoro Start")
    print("2) Pomodoro Break")
    print("3) Leave Pomodoro Timer")

    choice = input("\n> ")



    def start_25_min():
        seconds_remaining = 1500

        while True:
            min = seconds_remaining // 60
            sec = seconds_remaining % 60
            print(min,":",sec, end="\r")

            seconds_remaining -= 1
            time.sleep(1)

            if seconds_remaining == 0:
                break 
    
    def break_timer():
        seconds_remaining = 300

        while True:
            min = seconds_remaining // 60
            sec = seconds_remaining % 60
            print(min, ":", sec, end="\r")

            seconds_remaining -= 1
            time.sleep(1)

            if seconds_remaining == 0:
                break
    
    if choice == '1':
        start_25_min()
    elif choice == '2':
        break_timer()
    elif choice == '3':
        print("Goodbye (ЯИЁ^ >сЃ«<^Рѓј")
    else:
        print("Invalid input. Please try again!")


